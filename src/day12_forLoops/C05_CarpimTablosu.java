package day12_forLoops;

public class C05_CarpimTablosu {
    public static void main(String[] args) {
        //

        int satir = 4;
        int sutun=5;

        for (int i = 1; i <=satir ; i++) {
            for (int j = 1; j <=sutun ; j++) {

                System.out.print(i*j + " ");
            }
            System.out.println("");
        }

        }
    }

